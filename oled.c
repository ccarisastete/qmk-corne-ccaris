static uint32_t glitch_timer = 0;
#define GLITCH_DURATION 1000  // Aumentado para reducir la frecuencia
#define MAX_GLITCH_BYTES 32  // Limitamos la cantidad de bytes a modificar

static void glitch_slave(void) {
    if (timer_elapsed32(glitch_timer) > GLITCH_DURATION) {
        glitch_timer = timer_read32();
        
        // Modificar directamente en la pantalla OLED
        uint8_t num_glitches = (rand() % 5) + 1;  // Reducido el número de glitches
        
        for(uint8_t i = 0; i < num_glitches; i++) {
            uint16_t pos = rand() % (ART_SIZE);  // Reducido el tamaño del glitch
            uint8_t glitch_byte = rand() & 0xFF;
            
            // Aplicar glitch directamente
            oled_write_raw_P(ART, pos);
            oled_write_char(glitch_byte, false);
        }
    }
    
    // Restaurar la imagen original
    oled_write_raw_P(ART, ART_SIZE);
}

